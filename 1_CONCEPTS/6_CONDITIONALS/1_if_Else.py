"""
Conditional statements in Python allow you to execute specific blocks of code based on whether a condition is true or false. 
The main conditional statements in Python are if, elif, and else.

"""


age = int(input("Enter your age: "))
if(age >= 18):
    print("Yes, you can drive\n")
elif(age == 1, 2, 3, 4, 5, 6, 7, 8, 9):
    print("You are a baby!\n")
elif(age == 10):
    print("Oh! Common Kiddo! You need to be 18+ to get a driving license & drive\n")
else:
    print("No, you can go home\n")

print("End of program")