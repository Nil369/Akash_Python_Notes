# A data type is an attribute that tells a computer how to interpret the value
# Python is a fantastic language that automatically identifies the type of data for us. 

"""
These are the following Data types in Python: 
1. Integers
2. Floating point numbers
3. Strings
4. Booleans
5. None 
"""

a = 420 # It is an integer data type identified by python automatically
print(type(a)) # checks the type of data type. => class <'int'>

b = 3.14 # similarly it identifies b as class <float> 
print(type(b)) 

c = "Hello, World!" # It is a string data type
print(type(c))

d = True # It is a boolean data type
print(type(d))

e = None # It is a special data type which represents absence of value.
print(type(e))
